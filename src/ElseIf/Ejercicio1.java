package ElseIf;

public class Ejercicio1 {
    public static void main(String[] args) {
        int num = 50;

        if (num > 10) {
            System.out.println("El numero es mayor que 10");
        } else if (num == 10) {
            System.out.println("El numero es igual a 10");
        } else {
            System.out.println("El numero es menor que 10");
        }
    }
}
